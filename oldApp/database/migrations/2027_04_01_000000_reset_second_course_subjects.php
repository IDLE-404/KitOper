        $seen = [];
        foreach ($lines as $line) {
            $line = trim($line);
            if ($line === '') {
                continue;
            }
            $parts = preg_split('/\\t+/u', $line);
            if (!$parts || count($parts) < 2) {
                continue;
            }

            $subjectRaw = trim($parts[0]);
            $teacherRaw = trim(implode("\t", array_slice($parts, 1)));
            if ($subjectRaw === '') {
                continue;
            }

            $subjectKey = $this->normalizeSubject($subjectRaw);
            $subjectId = $subjectIndex[$subjectKey] ?? null;
            if (!$subjectId) {
                continue;
            }

            $teacherName = $teacherRaw;
            if (preg_match('/\\b(вакансия|практика)\\b/iu', $teacherRaw)) {
                $split = preg_split('/[\\\\\\/]/u', $teacherRaw);
                $candidate = trim((string) end($split));
                if ($candidate !== '' && !preg_match('/^вакансия$/iu', $candidate)) {
                    $teacherName = $candidate;
                } else {
                    $teacherName = '';
                }
            }

            if ($teacherName === '') {
                continue;
            }

            $teacherKey = $this->normalizeTeacher($teacherName);
            $teacherId = $teacherIndex[$teacherKey] ?? null;
            if (!$teacherId) {
                continue;
            }

            $pairKey = $subjectId . ':' . $teacherId;
            if (isset($seen[$pairKey])) {
                continue;
            }
            $seen[$pairKey] = true;

            $pairs[] = [
                'subject_id' => $subjectId,
                'teacher_id' => $teacherId,
            ];
        }

        return $pairs;
